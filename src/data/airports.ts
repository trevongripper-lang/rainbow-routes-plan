const BY_IATA = new Map<string, Airport>(AIRPORTS.map((a) => [a.iata.toUpperCase(), a]));

export function findAirport(codeOrText: string | null | undefined): Airport | undefined {
  if (!codeOrText) return undefined;
  const code = codeOrText.trim().toUpperCase();
  return BY_IATA.get(code);
}

export function airportLabel(a: Airport): string {
  return `${a.iata} · ${a.city} (${a.name})`;
}

export function searchAirports(query: string, limit = 8): Airport[] {
  const q = query.trim().toLowerCase();
  if (!q) return AIRPORTS.slice(0, limit);
  const exact = BY_IATA.get(q.toUpperCase());
  const results: Airport[] = [];
  if (exact) results.push(exact);
  for (const a of AIRPORTS) {
    if (results.length >= limit) break;
    if (a === exact) continue;
    const hay = `${a.iata} ${a.city} ${a.country} ${a.name}`.toLowerCase();
    if (hay.includes(q)) results.push(a);
  }
  return results;
}
